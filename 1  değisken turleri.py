# Toplamı ekrana yazdırma
print(3 + 3)  # 3+3 toplandı

# 3'ün tipini ekrana yazdırma
print(type(3))

# 3.4'ün tipini ekrana yazdırma
print(type(3.4))  # Cevabın tipini yazdı

# KDV hesaplama
print(7000 + (7000 * 0.18))

# Ürün fiyatları
urun1 = 7000
urun2 = 8600
kdv = 0.18

# Ürünlerin KDV'li fiyatlarını hesaplama ve ekrana yazdırma
print(urun1 + (urun1 * kdv)) 
print(urun2 + (urun2 * kdv))

# İsim değişkeninin tipini ekrana yazdırma
name = "levent"
print(type(name))

# Öğrenci olup olmadığını belirten değişkenin tipini ekrana yazdırma
isStudent = True
print(type(isStudent))


